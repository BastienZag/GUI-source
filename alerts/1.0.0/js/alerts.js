(function(App) {

	var module = {};

	//------------------------------------------------------------------------------------------------------------------------------------------------------------
	// module init method
	//------------------------------------------------------------------------------------------------------------------------------------------------------------
	module.init = function() {
		App.debugging( 'alerts: Initiating', 'report' );


		$('.js-alertclose').on('click', function closeAlert() {
			App.debugging( 'alerts: Closing alert', 'interaction' );

			var $parent = $(this).parent('.alert');

			$parent
				.addClass('is-closed')
				.attr('aria-hidden', 'true');
		});
	};


	App.alerts = module;


	// run module
	App.alerts.init();

}(App));